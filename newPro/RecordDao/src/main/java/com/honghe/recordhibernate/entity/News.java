package com.honghe.recordhibernate.entity;

import javax.persistence.*;
import java.util.Date;
import java.util.Set;

import static javax.persistence.GenerationType.IDENTITY;

/**
 * Created by lch on 2015/2/12.
 */
@Entity
@Table(name = "news")
public class News {
    private int nId;
    private String nCont;
    private Date nBegintime;
    private Date nEndtime;
    private Integer uid;
    private String username;
    private String nShowtype;
    private String nFont;
    private String nFontsize;
    private String nFontcolor;
    private Date nTimeline;
    private String nLoop;
    private String nStart;
    private String nFinish;
    private Integer nWeek;
    private Integer nMonth;
    private Integer nType;
    private Integer nStatus;
    private String nDevicetype;

    @Id
    @GeneratedValue(strategy = IDENTITY)
    @Column(name = "n_id")
    public int getnId() {
        return nId;
    }
    public void setnId(int nId) {
        this.nId = nId;
    }
    @Basic
    @Column(name = "n_cont")
    public String getnCont() {
        return nCont;
    }
    public void setnCont(String nCont) {
        this.nCont = nCont;
    }
    @Basic
    @Column(name = "n_begintime")
    public Date getnBegintime() {
        return nBegintime;
    }
    public void setnBegintime(Date nBegintime) {
        this.nBegintime = nBegintime;
    }
    @Basic
    @Column(name = "n_endtime")
    public Date getnEndtime() {
        return nEndtime;
    }
    public void setnEndtime(Date nEndtime) {
        this.nEndtime = nEndtime;
    }
    @Basic
    @Column(name = "uid")
    public Integer getUid() {
        return uid;
    }
    public void setUid(Integer uid) {
        this.uid = uid;
    }
    @Basic
    @Column(name = "username")
    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    @Basic
    @Column(name = "n_showtype")
    public String getnShowtype() {
        return nShowtype;
    }
    public void setnShowtype(String nShowtype) {
        this.nShowtype = nShowtype;
    }
    @Basic
    @Column(name = "n_fontsize")
    public String getnFontsize() {
        return nFontsize;
    }
    public void setnFontsize(String nFontsize) {
        this.nFontsize = nFontsize;
    }
    @Basic
    @Column(name = "n_fontcolor")
    public String getnFontcolor() {
        return nFontcolor;
    }
    public void setnFontcolor(String nFontcolor) {
        this.nFontcolor = nFontcolor;
    }
    @Basic
    @Column(name = "n_timeline")
    public Date getnTimeline() {
        return nTimeline;
    }
    public void setnTimeline(Date nTimeline) {
        this.nTimeline = nTimeline;
    }
    @Basic
    @Column(name = "n_font")
    public String getnFont() {
        return nFont;
    }

    public void setnFont(String nFont) {
        this.nFont = nFont;
    }

    @Basic
    @Column(name = "n_loop")
    public String getnLoop() {
        return nLoop;
    }

    public void setnLoop(String nLoop) {
        this.nLoop = nLoop;
    }
    @Basic
    @Column(name = "n_start")
    public String getnStart() {
        return nStart;
    }

    public void setnStart(String nStart) {
        this.nStart = nStart;
    }
    @Basic
    @Column(name = "n_finish")
    public String getnFinish() {
        return nFinish;
    }

    public void setnFinish(String nFinish) {
        this.nFinish = nFinish;
    }
    @Basic
    @Column(name = "n_week")
    public Integer getnWeek() {
        return nWeek;
    }

    public void setnWeek(Integer nWeek) {
        this.nWeek = nWeek;
    }
    @Basic
    @Column(name = "n_month")
    public Integer getnMonth() {
        return nMonth;
    }

    public void setnMonth(Integer nMonth) {
        this.nMonth = nMonth;
    }
    @Basic
    @Column(name = "n_type")
    public Integer getnType() {
        return nType;
    }

    public void setnType(Integer nType) {
        this.nType = nType;
    }

    @Basic
    @Column(name = "n_status")
    public Integer getnStatus() {
        return nStatus;
    }

    public void setnStatus(Integer nStatus) {
        this.nStatus = nStatus;
    }
    @Basic
    @Column(name = "n_devicetype")

    public String getnDevicetype() {
        return nDevicetype;
    }

    public void setnDevicetype(String nDevicetype) {
        this.nDevicetype = nDevicetype;
    }
}
